use base64::{alphabet, Engine};
use base64::engine::general_purpose::PAD;
use base64::engine::GeneralPurpose;

const SECRETS: &[u8] = &[0xCB, 0xE2, 0xE4, 0xD3, 0xCE, 0xC3, 0x9B, 0x9B, 0xC9, 0x98, 0xFC, 0xD3, 0xCC, 0xE2, 0xE4, 0xC7, 0xE7, 0xFD, 0x9F, 0xEC, 0xE5, 0xFD, 0xE0, 0xDB, 0xFF, 0xED, 0xE4, 0x92, 0xCB, 0xF2, 0xE8, 0xC0, 0xE6, 0xF2, 0xFC, 0xD0, 0xF0, 0xF2, 0xE0, 0x92, 0xCB, 0xF2, 0xE0, 0xDB, 0xE4, 0xC1, 0xCE, 0xD9, 0xCE, 0xC0, 0xF0, 0xE1, 0xE7, 0xEB, 0x97, 0x97];

fn xor_deobfuscate(input: &[u8], key: u8) -> String {
    String::from_utf8(input.iter().map(|&byte| byte ^ key).collect()).unwrap()
}

#[allow(dead_code)]
fn xor_obfuscate(input: &str, key: u8) -> Vec<u8> {
    input.bytes().map(|byte| byte ^ key).collect()
}

pub fn get_secrets() -> (String, String) {
    let deobfuscated = xor_deobfuscate(SECRETS, 0xAA);
    let engine = GeneralPurpose::new(&alphabet::STANDARD, PAD);
    let decoded = String::from_utf8(engine.decode(&deobfuscated).unwrap()).unwrap();
    let parts = decoded.split('|').collect::<Vec<&str>>();
    (parts[0].to_string(), parts[1].to_string())
}
